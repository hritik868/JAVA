class operator{
		public static void main(String[] args){
		int a = 6;
		int b = 7;
		int sum = a+b;
		System.out.println("a+b = "+sum);
		System.out.println("a+b = "+ a+b);
		System.out.println(a+b);
		System.out.println(sum);
		System.out.println(a++);
		System.out.println(++a);
		System.out.println(a);
		}
	}