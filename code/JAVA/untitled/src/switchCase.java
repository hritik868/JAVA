public class switchCase {
    public static void main(String[] args) {
        int x = 11;

        switch (x){

            case 10:
                System.out.println("Its 10");
                break;

            case 13:
                System.out.println("Its 13");
            break;

            default:
                System.out.println("Its Default");
                break;

            case 15:
                System.out.println("Its 15");
            break;


        }
    }
}
